     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $gallery = Gallery::locale()->isActive()->findOrFail($id);
        $images = $gallery->items->map(function ($item) {
            return [
                'thumb' => '/get_image/330/330/fit' . $item->image,
                'src'   => $item->image,
                'title' => $item->title,
            ];
        })->toArray();

        return view(getSiteBladePath('modules.gallery.show'), compact('images', 'gallery'));
    }

